-- Direct format channel counts for ordering targets
local _DIRECT_ORDER = { "mono", "stereo", "lcr", "quad", "cube", "cube_noc", "5.1", "7.1", "7.1.4", "9.1.6" }

-- Mono target key mappings: "mono_left" → s2m="left", etc.
local _MONO_KEYS = {
  mono_left  = { format = "mono", s2m = "left",       label = "Mono-L" },
  mono_right = { format = "mono", s2m = "right",      label = "Mono-R" },
  mono_lr    = { format = "mono", s2m = "downmix",    label = "Mono-LR" },
  mono_lcr   = { format = "mono", s2m = "downmix_lcr", label = "Mono-LCR" },
}

-- Parse compound target key. Returns (format, conversion_mode, mono_info_or_nil).
-- Examples: "7.1:extract" → ("7.1", "extract", nil)
--           "mono_left" → ("mono", "downmix", {s2m="left", label="Mono (Left)"})
--           "mono_lr:extract" → ("mono", "extract", {s2m="downmix", label="Mono (LR)"})
local function parse_target_key(key)
  if key == "__sep_extract__" then return nil, nil, nil end
  -- Check for extract suffix first
  local base, mode = key:match("^(.+):extract$")
  if not base then base, mode = key, "downmix" else mode = "extract" end
  -- Check for mono variant
  local mono = _MONO_KEYS[base]
  if mono then return "mono", mode, mono end
  -- Plain format key
  return base, mode, nil
end

local function compute_valid_targets(resolved_format)
  local def = FORMAT_DEFS[resolved_format]
  if not def then return {} end

  if def.category == "ambisonic" then
    -- Lower ambisonic orders + mono + direct decode targets
    local targets = {}
    for key, fdef in pairs(FORMAT_DEFS) do
      if fdef.category == "ambisonic" and fdef.order and fdef.order < def.order then
        targets[#targets + 1] = key
      end
    end
    -- Ambisonic decode to direct speaker layouts (data-driven from AMBI_DECODE)
    local decode_table = AMBI_DECODE[def.order]
    if decode_table then
      for layout in pairs(decode_table) do
        targets[#targets + 1] = layout
      end
      -- cube_noc NOT offered for ambi decode (identical to cube; No C distinction is irrelevant)
    end
    targets[#targets + 1] = "mono"
    -- Sort by order descending, direct formats after ambi, mono last
    table.sort(targets, function(a, b)
      local da, db = FORMAT_DEFS[a], FORMAT_DEFS[b]
      if da.category ~= db.category then
        return da.category == "ambisonic"
      end
      if da.category == "ambisonic" then
        return (da.order or 0) > (db.order or 0)
      end
      return (da.channels or 0) > (db.channels or 0)
    end)
    return targets
  end

  -- Direct: any direct format with fewer channels
  -- LCR only valid from formats with a center channel
  local _HAS_CENTER = { lcr=true, ["5.1"]=true, ["7.1"]=true, ["7.1.4"]=true, ["9.1.6"]=true }
  local has_center = _HAS_CENTER[resolved_format]
  local targets = {}
  for _, key in ipairs(_DIRECT_ORDER) do
    local fdef = FORMAT_DEFS[key]
    if fdef.channels < def.channels then
      if key == "mono" then
        targets[#targets + 1] = "mono"  -- downmix: all channels fold to one
      elseif key == "lcr" and not has_center then
        -- skip: LCR only from center-channel formats
      else
        targets[#targets + 1] = key
      end
    end
  end
  -- Quad can encode to FOA (same channel count, different domain)
  if resolved_format == "quad" then
    targets[#targets + 1] = "ambi1"
  end
  -- Reverse so larger targets come first (stereo before mono variants), ambi at end
  local rev = {}
  for i = #targets, 1, -1 do rev[#rev + 1] = targets[i] end

  -- Append extract/isolate targets for direct formats (channel-based only, no ambi)
  if def.category == "direct" then
    local ext_targets = {}
    for _, key in ipairs(_DIRECT_ORDER) do
      local fdef = FORMAT_DEFS[key]
      if fdef.channels < def.channels then
        if key == "mono" then
          -- Flatten mono extract options
          ext_targets[#ext_targets + 1] = "mono_left"
          ext_targets[#ext_targets + 1] = "mono_right"
          -- mono_lr only meaningful for 3+ channels (for stereo, identical to downmix Mono)
          if def.channels > 2 then
            ext_targets[#ext_targets + 1] = "mono_lr"
          end
          if has_center then ext_targets[#ext_targets + 1] = "mono_lcr" end
        elseif key == "lcr" and not has_center then
          -- skip: LCR only from center-channel formats
        elseif key == "cube_noc" then
          -- skip: identical to cube for extract
        else
          ext_targets[#ext_targets + 1] = key
        end
      end
    end
    if #ext_targets > 0 then
      rev[#rev + 1] = "__sep_extract__"
      -- Reverse so larger targets come first (matching downmix order)
      for i = #ext_targets, 1, -1 do
        rev[#rev + 1] = ext_targets[i] .. ":extract"
      end
    end
  end

  return rev
end

local function classify_file(info, default_interp)
  -- Returns: status, resolved_format, is_ambiguous, ambiguous_options, status_reason, user_confirmed

  if info.audio_format ~= 1 then
    return "red", nil, false, nil, "Non-PCM format (format code " .. info.audio_format .. ")", false
  end

  local ch = info.channels
  if ch == 1 then
    return "red", nil, false, nil, "Mono input — cannot process", false
  end

  local candidates = CHANNEL_MAP[ch]
  if not candidates then
    return "red", nil, false, nil, "Unsupported: " .. ch .. " channels", false
  end

  if #candidates == 1 then
    return "teal", candidates[1], false, nil, nil, false
  end

  -- Ambiguous: apply default from settings
  local default_key = default_interp[ch]
  if default_key then
    return "orange", default_key, true, candidates, nil, false
  end

  -- Fallback: use first candidate as default
  return "orange", candidates[1], true, candidates, nil, false
end

local function add_file(state, filepath)
  -- Deduplicate
  for _, f in ipairs(state.files) do
    if f.path == filepath then return end
  end

  local info = wav_io.read_wav_info(filepath)
  if not info then
    local fname = filepath:match("[/\\]([^/\\]+)$") or filepath
    state.footer_warning = "Not a valid WAV file: " .. fname
    state.footer_warning_ts = R.time_precise()
    return
  end

  local filename = filepath:match("[/\\]([^/\\]+)$") or filepath
  local folder   = filepath:match("^(.*)[/\\]") or ""

  local status, resolved, is_ambiguous, ambig_opts, reason, confirmed =
    classify_file(info, state.default_interp)

  local valid_targets = {}
  if resolved then
    valid_targets = compute_valid_targets(resolved)
  end

  -- Parse first target to initialize format, mode, and mono sub-mode
  local init_fmt, init_mode, init_mono = nil, "downmix", nil
  if valid_targets[1] and valid_targets[1] ~= "__sep_extract__" then
    init_fmt, init_mode, init_mono = parse_target_key(valid_targets[1])
  end

  local entry = {
    path            = filepath,
    filename        = filename,
    folder          = folder,
    channels        = info.channels,
    sample_rate     = info.sample_rate,
    bits_per_sample = info.bits_per_sample,
    audio_format    = info.audio_format,
    data_offset     = info.data_offset,
    data_size       = info.data_size,
    duration        = info.duration,
    -- Format state
    status          = status,
    status_reason   = reason,
    detected_format = resolved,
    is_ambiguous    = is_ambiguous,
    ambiguous_options = ambig_opts,
    resolved_format = resolved,
    user_confirmed  = confirmed,
    target_format   = init_fmt,
    target_confirmed = false,
    conversion_mode = init_mode,
    valid_targets   = valid_targets,
    stereo_to_mono_mode = init_mono and init_mono.s2m or state.stereo_to_mono,
    s2m_confirmed   = false,
  }

  state.files[#state.files + 1] = entry

  -- Sort by filename
  table.sort(state.files, function(a, b)
    return a.filename:lower() < b.filename:lower()
  end)

  -- Clear stale process results
  state.process_results = {}
end

local function can_process_file(file)
  return file.status == "teal" or file.status == "orange"
end

-- Apply source format selection to a file and propagate to multi-selected peers
-- Ensure file.target_format + conversion_mode are still valid for the current valid_targets.
-- Resets to first downmix target if stale (e.g. after source format reinterpretation).
-- Reconstruct the compound key from a file's current target state
local function rebuild_target_key(file)
  if not file.target_format then return nil end
  local base
  if file.target_format == "mono" and file.stereo_to_mono_mode then
    local s2m = file.stereo_to_mono_mode
    base = "mono_" .. (s2m == "downmix" and "lr" or s2m == "downmix_lcr" and "lcr" or s2m)
  else
    base = file.target_format
  end
  if file.conversion_mode == "extract" then return base .. ":extract" end
  return base
end

-- Reset a file's target to the first valid option (parsing compound keys)
local function reset_file_target(file)
  local first = file.valid_targets[1]
  if first and first ~= "__sep_extract__" then
    local fmt, mode, mono = parse_target_key(first)
    file.target_format = fmt
    file.conversion_mode = mode
    if mono then file.stereo_to_mono_mode = mono.s2m end
  else
    file.target_format = nil
    file.conversion_mode = "downmix"
  end
  file.target_confirmed = false
end

local function validate_file_target(file)
  if not file.target_format then
    reset_file_target(file)
    return
  end
  local existing_key = rebuild_target_key(file)
  if existing_key then
    for _, vt in ipairs(file.valid_targets) do
      if vt == existing_key then return end  -- still valid
    end
  end
  -- Stale: reset to first target
  reset_file_target(file)
end

local function apply_format_selection(state, idx, opt_key)
  local file = state.files[idx]
  file.resolved_format = opt_key
  file.user_confirmed = true
  file.status = "teal"
  file.valid_targets = compute_valid_targets(opt_key)
  validate_file_target(file)
  -- Propagate to multi-selected ambiguous rows with same channel count
  if state.selected_indices[idx] then
    for si, _ in pairs(state.selected_indices) do
      if si ~= idx then
        local sf = state.files[si]
        if sf and sf.is_ambiguous and sf.channels == file.channels then
          local ok = false
          for _, ao in ipairs(sf.ambiguous_options or {}) do
            if ao == opt_key then ok = true; break end
          end
          if ok then
            sf.resolved_format = opt_key
            sf.user_confirmed = true
            sf.status = "teal"
            sf.valid_targets = compute_valid_targets(opt_key)
            validate_file_target(sf)
          end
        end
      end
    end
  end
end

-- Apply target format selection to a file and propagate to multi-selected peers
local function apply_target_selection(state, idx, tgt_key)
  local file = state.files[idx]
  local fmt, mode, mono = parse_target_key(tgt_key)
  file.target_format = fmt
  file.conversion_mode = mode
  file.target_confirmed = true
  if mono then file.stereo_to_mono_mode = mono.s2m; file.s2m_confirmed = true end
  if state.selected_indices[idx] then
    for si, _ in pairs(state.selected_indices) do
      if si ~= idx then
        local sf = state.files[si]
        if sf and sf.status ~= "red" then
          local ok = false
          for _, vt in ipairs(sf.valid_targets or {}) do
            if vt == tgt_key then ok = true; break end
          end
          if ok then
            sf.target_format = fmt
            sf.conversion_mode = mode
            sf.target_confirmed = true
            if mono then sf.stereo_to_mono_mode = mono.s2m; sf.s2m_confirmed = true end
          end
        end
      end
    end
  end
end

local function count_processable(state)
  local n = 0
  for _, f in ipairs(state.files) do
    if can_process_file(f) and f.target_format then
      n = n + 1
    end
  end
  return n
end

