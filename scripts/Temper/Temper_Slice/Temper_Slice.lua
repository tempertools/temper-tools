        end
      end
      if skip_count > 0 and wav_count == 0 then
        state.footer_warning = skip_count .. " file(s) skipped — only WAV supported"
        state.footer_warning_ts = R.time_precise()
      elseif skip_count > 0 then
        state.footer_warning = skip_count .. " non-WAV file(s) skipped"
        state.footer_warning_ts = R.time_precise()
      end
    end
    R.ImGui_EndDragDropTarget(ctx)

    -- Custom teal border covering full window
    local dl = R.ImGui_GetForegroundDrawList(ctx)
    R.ImGui_DrawList_AddRect(dl, win_x + 1, win_y + 1,
      win_x + win_w - 1, win_y + win_h - 1, SC.PRIMARY, 0, 0, 2.0)
  end
end

-- ── Settings persistence ──────────────────────────────────────────
local function load_settings(state)
  local function get(key) return R.GetExtState(_NS, key) end

  local v = get("output_mode")
  if v ~= "" then state.output_mode = v end

  v = get("input_ordering")
  if v ~= "" then state.input_ordering = v end

  v = get("output_ordering")
  if v ~= "" then state.output_ordering = v end

  v = get("ambi_input_format")
  if v ~= "" then state.ambi_input_format = v end

  v = get("downmix_preset")
  if v ~= "" then state.downmix_preset = v end

  v = get("stereo_to_mono")
  if v ~= "" then state.stereo_to_mono = v end

  -- Custom attenuation
  for _, key in ipairs({ "sides", "rears", "top_front", "top_back" }) do
    v = get("custom_atten_" .. key)
    if v ~= "" then state.custom_atten[key] = tonumber(v) or state.custom_atten[key] end
  end

  -- Default interpretations
  for _, ch in ipairs({ 4, 8, 16 }) do
    v = get("default_interp_" .. ch)
    if v ~= "" then state.default_interp[ch] = v end
  end
end

local function save_settings(state)
  local function set(key, val) R.SetExtState(_NS, key, tostring(val), true) end

  set("output_mode", state.output_mode)
  set("input_ordering", state.input_ordering)
  set("output_ordering", state.output_ordering)
  set("ambi_input_format", state.ambi_input_format)
  set("downmix_preset", state.downmix_preset)
  set("stereo_to_mono", state.stereo_to_mono)

  for _, key in ipairs({ "sides", "rears", "top_front", "top_back" }) do
    set("custom_atten_" .. key, string.format("%.1f", state.custom_atten[key]))
  end

  for _, ch in ipairs({ 4, 8, 16 }) do
    set("default_interp_" .. ch, state.default_interp[ch])
  end

  set("settings_version", "1")
end

-- ── Entry point ────────────────────────────────────────────────────
do
  if not check_instance_guard() then return end

  -- Guard ReaImGui's short-lived-resource rate limit (see Temper_Vortex.lua).
  local _ctx_ok, ctx = pcall(R.ImGui_CreateContext, "Temper Slice##slice")
  if not _ctx_ok or not ctx then
    reaper.ShowMessageBox(
      "Temper Slice could not start because ReaImGui is still cleaning " ..
      "up from a previous instance.\n\n" ..
      "Close any existing Slice window, wait ~15 seconds, then try again.\n" ..
      "If it keeps happening, restart REAPER.",
      "Temper Slice", 0)
    return
  end
  if type(temper_theme) == "table" then temper_theme.attach_fonts(ctx) end

  if lic then lic.configure({
    namespace    = "TEMPER_Slice",
    scope_id     = 0x7,
    display_name = "Slice",
    buy_url      = "https://www.tempertools.com/scripts/slice",
  }) end

  local state = {
    files            = {},
    selected_indices = {},
    last_click_idx   = nil,
    output_mode      = "folder",
    output_dir       = "",
    status           = "idle",
    process_idx      = 0,
    process_total    = 0,
    process_results  = {},
    process_state    = nil,
    process_done_count = 0,
    process_current_file = nil,
    process_current_path = nil,
    done_ts          = 0,
    cancel_requested = false,
    should_close     = false,
    footer_warning   = nil,
    footer_warning_ts = nil,
    settings_open    = false,
    input_ordering  = "smpte",
    output_ordering = "smpte",
    ambi_input_format = "ambix",  -- "ambix" (ACN/SN3D), "acn_n3d", "fuma" (FuMa/maxN)
    downmix_preset   = "itu",
    custom_atten     = { sides = -3.0, rears = -3.0, top_front = -3.0, top_back = -3.0 },
    default_interp   = { [4] = "quad", [8] = "7.1", [16] = "9.1.6" },
    stereo_to_mono   = "downmix",
    -- Action button flash (keyboard dispatch feedback, 250ms)
    _btn_flash       = {},
  }

  load_settings(state)

  local win_flags = R.ImGui_WindowFlags_NoCollapse()
                  | R.ImGui_WindowFlags_NoTitleBar()
                  | R.ImGui_WindowFlags_NoScrollbar()
                  | R.ImGui_WindowFlags_NoScrollWithMouse()

  -- Action framework wiring (IPC via ExtState; see lib/temper_actions.lua)
  local _BTN_FLASH_DUR = 0.25
  local function _set_flash(k) state._btn_flash[k] = R.time_precise() + _BTN_FLASH_DUR end

  local HANDLERS = {
    slice             = function() _set_flash("slice");             slice_actions.slice(state)             end,
    cancel            = function() _set_flash("cancel");            slice_actions.cancel(state)            end,
    clear             = function() _set_flash("clear");             slice_actions.clear(state)             end,
    cycle_output_mode = function() _set_flash("cycle_output_mode"); slice_actions.cycle_output_mode(state) end,
    remove_selected   = function() _set_flash("remove_selected");   slice_actions.remove_selected(state)   end,
    close             = function() state.should_close = true end,
  }

  rsg_actions.clear_pending_on_init(_NS)

  local _first_loop = true

  local function loop()
    R.SetExtState(_NS, "instance_ts", tostring(R.time_precise()), false)
    rsg_actions.heartbeat(_NS)
    local _focus_requested = rsg_actions.poll(_NS, HANDLERS)
    tick_state(state)

    if not _first_loop then
      R.ImGui_SetNextWindowSizeConstraints(ctx,
        CONFIG.min_win_w, CONFIG.min_win_h, 4096, 4096)
    end
    _first_loop = false

    R.ImGui_SetNextWindowSize(ctx, CONFIG.win_w, CONFIG.win_h,
      R.ImGui_Cond_FirstUseEver())

    if _focus_requested then
      R.ImGui_SetNextWindowFocus(ctx)
      if R.APIExists("JS_Window_SetForeground") then
        local hwnd = R.JS_Window_Find("Temper Slice", true)
        if hwnd then R.JS_Window_SetForeground(hwnd) end
      end
    end

    local n_theme = temper_theme and temper_theme.push(ctx) or 0

    local visible, open = R.ImGui_Begin(ctx, "Temper Slice##slice_main", true, win_flags)
    if visible then
      local lic_status = lic and lic.check("SLICE", ctx)

      if lic_status == "expired" then
        R.ImGui_Spacing(ctx)
        R.ImGui_TextColored(ctx, SC.ERROR_RED, "  Your Slice trial has expired.")
        R.ImGui_Spacing(ctx)
        R.ImGui_TextDisabled(ctx, "  Purchase a license at tempertools.com to continue.")
        if not lic.is_dialog_open() then lic.open_dialog(ctx) end
        lic.draw_dialog(ctx)
      else
        render_gui(ctx, state)
        if lic_status == "trial" then
          local days_left = lic.days_remaining and lic.days_remaining("SLICE")
          if days_left then
            R.ImGui_SetCursorPos(ctx, R.ImGui_GetWindowWidth(ctx) - 100, 10)
            R.ImGui_PushStyleColor(ctx, R.ImGui_Col_Text(), SC.TERTIARY)
            R.ImGui_Text(ctx, days_left .. "d trial")
            R.ImGui_PopStyleColor(ctx, 1)
          end
        end
        if lic and lic.is_dialog_open() then lic.draw_dialog(ctx) end
      end

      R.ImGui_End(ctx)
    end

    if temper_theme then temper_theme.pop(ctx, n_theme) end

    if open and not state.should_close then
      R.defer(loop)
    else
      save_settings(state)
      R.SetExtState(_NS, "instance_ts", "", false)
    end
  end

  if not _RSG_TEST_MODE then R.defer(loop) end
end
