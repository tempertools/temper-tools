    local r = state.process_results[file.path]
    if r and r.ok and r.peak_db then
      local tip = string.format("Peak: %.1f dB", r.peak_db)
      if r.clip_count > 0 then
        tip = tip .. string.format("  |  %d samples clipped", r.clip_count)
      end
