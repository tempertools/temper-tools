    if pct > 0 then
      R.ImGui_DrawList_AddRectFilled(dl, cx, cy,
        cx + bar_w * pct, cy + CONFIG.footer_h, SC.PRIMARY, 0)
    end

    -- Left text
    local left_text
    if state.status == "done" then
      -- Summarize clip stats
      local total_clips = 0
      for _, r in pairs(state.process_results) do
        if r.ok and r.clip_count and r.clip_count > 0 then
          total_clips = total_clips + r.clip_count
        end
      end
      left_text = "Complete — " .. state.process_done_count .. " files converted"
      if total_clips > 0 then
        left_text = left_text .. " (" .. total_clips .. " samples clipped)"
      end
    elseif state.process_current_file then
      left_text = "Converting " .. state.process_current_file.filename ..
        " (" .. state.process_done_count .. "/" .. state.process_total .. ")"
    else
      left_text = "Processing..."
    end
    R.ImGui_DrawList_AddText(dl, cx + 4, cy + 3, SC.TEXT_ON, left_text)

    -- Right text (percentage)
    local pct_text = string.format("%d%%", math.floor(pct * 100))
    local tw = R.ImGui_CalcTextSize(ctx, pct_text)
    R.ImGui_DrawList_AddText(dl, cx + bar_w - tw - 4, cy + 3, SC.TEXT_ON, pct_text)

    R.ImGui_Dummy(ctx, 0, CONFIG.footer_h)
  else
    -- Normal footer
    local left_text = "Ready"
    local left_col  = SC.TEXT_MUTED

    if state.footer_warning and state.footer_warning_ts then
      local elapsed = R.time_precise() - state.footer_warning_ts
      if elapsed < 3.0 then
        left_text = state.footer_warning
        left_col  = SC.TERTIARY
      else
        state.footer_warning = nil
        state.footer_warning_ts = nil
      end
    end

    R.ImGui_PushStyleColor(ctx, R.ImGui_Col_Text(), left_col)
    R.ImGui_Text(ctx, left_text)
    R.ImGui_PopStyleColor(ctx, 1)

    -- Right summary
    local n_total = #state.files
    local n_proc  = count_processable(state)
    local summary = n_total .. " files, " .. n_proc .. " processable"
    local tw = R.ImGui_CalcTextSize(ctx, summary)
    R.ImGui_SameLine(ctx, win_w - tw)
    R.ImGui_PushStyleColor(ctx, R.ImGui_Col_Text(), SC.TEXT_MUTED)
    R.ImGui_Text(ctx, summary)
    R.ImGui_PopStyleColor(ctx, 1)
  end

  if temper_theme and temper_theme.font_bold then R.ImGui_PopFont(ctx) end
end

local function handle_file_click(ctx, state, idx)
  local ctrl  = R.ImGui_IsKeyDown(ctx, R.ImGui_Mod_Ctrl())
  local shift = R.ImGui_IsKeyDown(ctx, R.ImGui_Mod_Shift())

  if shift and state.last_click_idx then
    local lo = math.min(state.last_click_idx, idx)
    local hi = math.max(state.last_click_idx, idx)
    if not ctrl then state.selected_indices = {} end
    for i = lo, hi do state.selected_indices[i] = true end
  elseif ctrl then
    state.selected_indices[idx] = state.selected_indices[idx] and nil or true
  else
    state.selected_indices = { [idx] = true }
  end

  if not shift then state.last_click_idx = idx end
end

remove_selected = function(state)
  if state.status == "processing" then return end
  local new_files = {}
  for idx, f in ipairs(state.files) do
    if not state.selected_indices[idx] then
      new_files[#new_files + 1] = f
    end
  end
  state.files = new_files
  state.selected_indices = {}
  state.last_click_idx = nil
end

local function render_file_row(ctx, state, idx, file, row_w)
