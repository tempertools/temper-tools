-- ── Downmix Coefficients (ITU-R BS.775 based) ─────────────────────
local _CENTER_GAIN   = 0.7071  -- -3dB
local _LFE_GAIN      = 0.0     -- excluded from downmix
local _SURROUND_GAIN = 0.7071  -- -3dB
local _HEIGHT_GAIN   = 0.7071  -- -3dB

-- Downmix chain: each format's next step down for cascade composition
local _DOWNMIX_CHAIN = {
  ["9.1.6"] = "7.1.4",
  ["7.1.4"] = "7.1",
  ["7.1"]   = "5.1",
  ["cube"]     = "quad",
  ["cube_noc"] = "quad",
  ["5.1"]   = "stereo",
  ["lcr"]   = "stereo",
  ["quad"]  = "stereo",
  ["stereo"] = "mono",
}

-- ── Extract mode: fixed -3dB fold for rear-to-side, no custom attenuation ──
local _EXTRACT_FOLD_GAIN = 0.7071  -- fixed -3dB

local _EXTRACT_CHAIN = {
  ["9.1.6"]    = "7.1.4",
  ["7.1.4"]    = "7.1",
  ["7.1"]      = "5.1",
  ["cube"]     = "quad",
  ["cube_noc"] = "quad",
  ["5.1"]      = "stereo",
  ["lcr"]      = "stereo",
  ["quad"]     = "stereo",
  ["stereo"]   = "mono",
}


-- Compose two mix matrices: src→intermediate (m1) then intermediate→dst (m2)
local function compose_matrices(m1, m2)
  local result = {}
  for dst_ch = 1, #m2 do
    result[dst_ch] = {}
    for _, pair2 in ipairs(m2[dst_ch]) do
      local int_ch, coeff2 = pair2[1], pair2[2]
      if m1[int_ch] then
        for _, pair1 in ipairs(m1[int_ch]) do
          local src_ch, coeff1 = pair1[1], pair1[2]
          local combined = coeff1 * coeff2
          local found = false
          for _, existing in ipairs(result[dst_ch]) do
            if existing[1] == src_ch then
              existing[2] = existing[2] + combined
              found = true
              break
            end
          end
          if not found then
            result[dst_ch][#result[dst_ch] + 1] = { src_ch, combined }
          end
        end
      end
    end
  end
  return result
end

local function resolve_output_path(file, target_format, conversion_mode, output_mode, output_dir)
  local base = file.filename:match("(.+)%.wav$") or file.filename
  local suffix = "_" .. (FORMAT_DEFS[target_format] and FORMAT_DEFS[target_format].label or target_format)
  suffix = suffix:gsub("%.", "_"):gsub(" ", "_")
  if conversion_mode == "extract" then suffix = suffix .. "_iso" end

  if output_mode == "delete" then
    return file.folder .. "/" .. file.filename .. ".tmp"
  elseif output_mode == "source" then
    return file.folder .. "/" .. base .. suffix .. ".wav"
  else
    return output_dir .. "/" .. base .. suffix .. ".wav"
  end
end

local function _build_mix_matrix_core(src_format, dst_format, settings)
  local src_def = FORMAT_DEFS[src_format]
  local dst_def = FORMAT_DEFS[dst_format]
  if not src_def or not dst_def then return nil end

  -- Resolve speaker group gains from settings (custom preset overrides ITU defaults)
  local _cg = _CENTER_GAIN  -- center always ITU
  local _sg, _rg, _tfg, _tbg  -- sides, rears, top_front, top_back
  if settings.downmix_preset == "custom" and settings.custom_atten then
    local ca = settings.custom_atten
    _sg  = 10 ^ ((ca.sides or -3.0) / 20)
    _rg  = 10 ^ ((ca.rears or -3.0) / 20)
    _tfg = 10 ^ ((ca.top_front or -3.0) / 20)
    _tbg = 10 ^ ((ca.top_back or -3.0) / 20)
  else
    _sg  = _SURROUND_GAIN
    _rg  = _SURROUND_GAIN
    _tfg = _HEIGHT_GAIN
    _tbg = _HEIGHT_GAIN
  end

  local matrix = {}
  for ch = 1, dst_def.channels do matrix[ch] = {} end

  -- Ambisonic order reduction: identity copy of first N channels
  if src_def.category == "ambisonic" and dst_def.category == "ambisonic" then
    for ch = 1, dst_def.channels do
      matrix[ch] = { { ch, 1.0 } }
    end
    return matrix
  end

  -- Ambisonic to mono: take W channel (channel 1)
  if src_def.category == "ambisonic" and dst_format == "mono" then
    matrix[1] = { { 1, 1.0 } }
    return matrix
  end

  -- Ambisonic decode to direct speaker layout (max-rE, energy-normalized)
  -- Coefficients from AMBI_DECODE table (Zotter & Frank 2019)
  if src_def.category == "ambisonic" and dst_def.category == "direct"
     and dst_format ~= "mono" then
    local order_table = AMBI_DECODE[src_def.order]
    local target = dst_format
    if target == "cube_noc" then target = "cube" end
    if order_table and order_table[target] then
      for ch, sources in pairs(order_table[target]) do
        matrix[ch] = sources
      end
      return matrix
    end
  end

  -- Quad -> FOA encode (pseudo-inverse of AMBI_DECODE[1]["quad"])
  -- Z channel is zeroed (Quad has no height speakers)
  if src_format == "quad" and dst_format == "ambi1" then
    for ch, sources in ipairs(QUAD_TO_FOA) do
      matrix[ch] = sources
    end
    return matrix
  end

  -- Stereo to mono
  if src_format == "stereo" and dst_format == "mono" then
    if settings.stereo_to_mono == "left" then
      matrix[1] = { { 1, 1.0 } }
    elseif settings.stereo_to_mono == "right" then
      matrix[1] = { { 2, 1.0 } }
    else -- downmix
      matrix[1] = { { 1, 0.5 }, { 2, 0.5 } }
    end
    return matrix
  end

  -- Direct to stereo downmix
  if dst_format == "stereo" then
    matrix[1] = {}  -- Left
    matrix[2] = {}  -- Right

    if src_format == "lcr" then
      matrix[1] = { { 1, 1.0 }, { 3, _cg } }
      matrix[2] = { { 2, 1.0 }, { 3, _cg } }

    elseif src_format == "quad" then
      -- Quad ch3,4 = BL,BR → rears
      matrix[1] = { { 1, 1.0 }, { 3, _rg } }
      matrix[2] = { { 2, 1.0 }, { 4, _rg } }

    elseif src_format == "5.1" then
      -- ch5,6 = Ls,Rs → sides
      matrix[1] = { { 1, 1.0 }, { 3, _cg }, { 5, _sg } }
      matrix[2] = { { 2, 1.0 }, { 3, _cg }, { 6, _sg } }

    elseif src_format == "7.1" then
      -- ch5,6 = Lss,Rss → sides; ch7,8 = Lrs,Rrs → rears
      matrix[1] = { { 1, 1.0 }, { 3, _cg }, { 5, _sg }, { 7, _rg } }
      matrix[2] = { { 2, 1.0 }, { 3, _cg }, { 6, _sg }, { 8, _rg } }

    elseif src_format == "7.1.4" then
      -- ch5,6 = sides; ch7,8 = rears; ch9,10 = top front; ch11,12 = top back
      matrix[1] = { { 1, 1.0 }, { 3, _cg }, { 5, _sg }, { 7, _rg },
                    { 9, _tfg }, { 11, _tbg } }
      matrix[2] = { { 2, 1.0 }, { 3, _cg }, { 6, _sg }, { 8, _rg },
                    { 10, _tfg }, { 12, _tbg } }

    elseif src_format == "9.1.6" then
      -- ch5,6 = sides; ch7,8 = rears; ch9,10 = wide (sides); ch11-16 = heights
      matrix[1] = { { 1, 1.0 }, { 3, _cg },
                    { 5, _sg }, { 7, _rg }, { 9, _sg },
                    { 11, _tfg }, { 13, _tfg }, { 15, _tbg } }
      matrix[2] = { { 2, 1.0 }, { 3, _cg },
                    { 6, _sg }, { 8, _rg }, { 10, _sg },
                    { 12, _tfg }, { 14, _tfg }, { 16, _tbg } }

    elseif src_format == "cube" or src_format == "cube_noc" then
      -- ch3,4 = BL,BR → rears; ch5,6 = TFL,TFR → top front; ch7,8 = TBL,TBR → top back
      matrix[1] = { { 1, 1.0 }, { 3, _rg }, { 5, _tfg }, { 7, _tbg } }
      matrix[2] = { { 2, 1.0 }, { 4, _rg }, { 6, _tfg }, { 8, _tbg } }
    end

    return matrix
  end

  -- Direct to mono (via stereo intermediate — two-stage)
  if dst_format == "mono" and src_def.category == "direct" and src_def.channels > 2 then
    -- Downmix LCR: direct average of L+R+C (no surround contribution)
    local _DM_HAS_CENTER = { lcr=true, ["5.1"]=true, ["7.1"]=true, ["7.1.4"]=true, ["9.1.6"]=true }
    if settings.stereo_to_mono == "downmix_lcr" and _DM_HAS_CENTER[src_format] then
      matrix[1] = { { 1, 1/3 }, { 2, 1/3 }, { 3, 1/3 } }
      return matrix
    end

    local stereo_matrix = _build_mix_matrix_core(src_format, "stereo", settings)
    if not stereo_matrix then return nil end

    matrix[1] = {}
    local s2m = settings.stereo_to_mono
    if s2m == "left" then
      for _, pair in ipairs(stereo_matrix[1]) do
        matrix[1][#matrix[1] + 1] = { pair[1], pair[2] }
      end
    elseif s2m == "right" then
      for _, pair in ipairs(stereo_matrix[2]) do
        matrix[1][#matrix[1] + 1] = { pair[1], pair[2] }
      end
    else
      -- Downmix: average of L and R stereo downmix
      for _, pair in ipairs(stereo_matrix[1]) do
        matrix[1][#matrix[1] + 1] = { pair[1], pair[2] * 0.5 }
      end
      for _, pair in ipairs(stereo_matrix[2]) do
        local found = false
        for _, existing in ipairs(matrix[1]) do
          if existing[1] == pair[1] then
            existing[2] = existing[2] + pair[2] * 0.5
            found = true
            break
          end
        end
        if not found then
          matrix[1][#matrix[1] + 1] = { pair[1], pair[2] * 0.5 }
        end
      end
    end
    return matrix
  end

  -- Direct-to-direct one-step downmixes (intermediate formats)
  if src_format == "7.1" and dst_format == "5.1" then
    -- SMPTE 7.1→5.1: fold rear surround into side surround
    matrix = {}
    for ch = 1, 6 do matrix[ch] = {} end
    matrix[1] = { { 1, 1.0 } }                            -- L
    matrix[2] = { { 2, 1.0 } }                            -- R
    matrix[3] = { { 3, 1.0 } }                            -- C
    matrix[4] = { { 4, 1.0 } }                            -- LFE
    matrix[5] = { { 5, 1.0 }, { 7, _rg } }                -- Ls = Lss + Lrs*gain
    matrix[6] = { { 6, 1.0 }, { 8, _rg } }                -- Rs = Rss + Rrs*gain
    return matrix
  end

  if src_format == "7.1.4" and dst_format == "7.1" then
    -- SMPTE 7.1.4→7.1: fold height to nearest ear-level by position
    -- Ltf/Rtf (top front) → L/R (front), Ltb/Rtb (top back) → Lrs/Rrs (rear)
    matrix = {}
    for ch = 1, 8 do matrix[ch] = {} end
    matrix[1] = { { 1, 1.0 }, { 9, _tfg } }               -- L + Ltf*gain
    matrix[2] = { { 2, 1.0 }, { 10, _tfg } }              -- R + Rtf*gain
    matrix[3] = { { 3, 1.0 } }                            -- C
    matrix[4] = { { 4, 1.0 } }                            -- LFE
    matrix[5] = { { 5, 1.0 } }                            -- Lss
    matrix[6] = { { 6, 1.0 } }                            -- Rss
    matrix[7] = { { 7, 1.0 }, { 11, _tbg } }              -- Lrs + Ltb*gain
    matrix[8] = { { 8, 1.0 }, { 12, _tbg } }              -- Rrs + Rtb*gain
    return matrix
  end

  if src_format == "7.1.4" and dst_format == "cube" then
    -- 7.1.4→4.0.4 Cube: C folds to L/R, LFE dropped,
    -- sides become BL/BR, rears fold onto sides, heights pass through
    -- 7.1.4: L(1),R(2),C(3),LFE(4),Lss(5),Rss(6),Lrs(7),Rrs(8),Ltf(9),Rtf(10),Ltb(11),Rtb(12)
    -- Cube:  FL(1),FR(2),BL(3),BR(4),TFL(5),TFR(6),TBL(7),TBR(8)
    matrix = {}
    for ch = 1, 8 do matrix[ch] = {} end
    matrix[1] = { { 1, 1.0 }, { 3, _cg } }                 -- FL = L + C*gain
    matrix[2] = { { 2, 1.0 }, { 3, _cg } }                 -- FR = R + C*gain
    matrix[3] = { { 5, 1.0 }, { 7, _rg } }                 -- BL = Lss + Lrs*gain
    matrix[4] = { { 6, 1.0 }, { 8, _rg } }                 -- BR = Rss + Rrs*gain
    matrix[5] = { { 9, 1.0 } }                                               -- TFL = Ltf
    matrix[6] = { { 10, 1.0 } }                                              -- TFR = Rtf
    matrix[7] = { { 11, 1.0 } }                                              -- TBL = Ltb
    matrix[8] = { { 12, 1.0 } }                                              -- TBR = Rtb
    return matrix
  end

  if src_format == "7.1.4" and dst_format == "cube_noc" then
    -- 7.1.4→4.0.4 (No C): center dropped, LFE dropped,
    -- sides become BL/BR, rears fold onto sides, heights pass through
    matrix = {}
    for ch = 1, 8 do matrix[ch] = {} end
    matrix[1] = { { 1, 1.0 } }                            -- FL = L (no center fold)
    matrix[2] = { { 2, 1.0 } }                            -- FR = R (no center fold)
    matrix[3] = { { 5, 1.0 }, { 7, _rg } }                -- BL = Lss + Lrs*gain
    matrix[4] = { { 6, 1.0 }, { 8, _rg } }                -- BR = Rss + Rrs*gain
    matrix[5] = { { 9, 1.0 } }                            -- TFL = Ltf
    matrix[6] = { { 10, 1.0 } }                           -- TFR = Rtf
    matrix[7] = { { 11, 1.0 } }                           -- TBL = Ltb
    matrix[8] = { { 12, 1.0 } }                           -- TBR = Rtb
    return matrix
  end

  if src_format == "9.1.6" and dst_format == "7.1.4" then
    -- SMPTE 9.1.6→7.1.4: fold wide+mid height into base
    matrix = {}
    for ch = 1, 12 do matrix[ch] = {} end
    matrix[1]  = { { 1, 1.0 }, { 9, _sg } }               -- L + Lw*gain (wide → sides)
    matrix[2]  = { { 2, 1.0 }, { 10, _sg } }              -- R + Rw*gain
    matrix[3]  = { { 3, 1.0 } }                           -- C
    matrix[4]  = { { 4, 1.0 } }                           -- LFE
    matrix[5]  = { { 5, 1.0 } }                           -- Lss
    matrix[6]  = { { 6, 1.0 } }                           -- Rss
    matrix[7]  = { { 7, 1.0 } }                           -- Lrs
    matrix[8]  = { { 8, 1.0 } }                           -- Rrs
    matrix[9]  = { { 11, 1.0 }, { 13, _tfg } }            -- Ltf + Ltm*gain
    matrix[10] = { { 12, 1.0 }, { 14, _tfg } }            -- Rtf + Rtm*gain
    matrix[11] = { { 15, 1.0 } }                          -- Ltb
    matrix[12] = { { 16, 1.0 } }                          -- Rtb
    return matrix
  end

  if src_format == "5.1" and dst_format == "lcr" then
    -- 5.1→LCR: fold surround into L/R, exclude LFE
    matrix = {}
    for ch = 1, 3 do matrix[ch] = {} end
    matrix[1] = { { 1, 1.0 }, { 5, _sg } }                -- L + Ls*gain
    matrix[2] = { { 2, 1.0 }, { 6, _sg } }                -- R + Rs*gain
    matrix[3] = { { 3, 1.0 } }                            -- C
    return matrix
  end

  if src_format == "5.1" and dst_format == "quad" then
    -- 5.1→Quad: fold center into L/R, exclude LFE
    matrix = {}
    for ch = 1, 4 do matrix[ch] = {} end
    matrix[1] = { { 1, 1.0 }, { 3, _cg } }                -- FL = L + C*gain
    matrix[2] = { { 2, 1.0 }, { 3, _cg } }                -- FR = R + C*gain
    matrix[3] = { { 5, 1.0 } }                            -- RL = Ls
    matrix[4] = { { 6, 1.0 } }                            -- RR = Rs
    return matrix
  end

  if (src_format == "cube" or src_format == "cube_noc") and dst_format == "quad" then
    -- Cube→Quad: fold heights to positional counterparts at equal gain
    -- Cube: FL(1), FR(2), BL(3), BR(4), TFL(5), TFR(6), TBL(7), TBR(8)
    -- Both top front and top back fold at same gain -- equal-power format to equal-power format
    local hg = _tfg  -- use top_front gain for all heights (equal treatment)
    matrix = {}
    for ch = 1, 4 do matrix[ch] = {} end
    matrix[1] = { { 1, 1.0 }, { 5, hg } }   -- FL + TFL
    matrix[2] = { { 2, 1.0 }, { 6, hg } }   -- FR + TFR
    matrix[3] = { { 3, 1.0 }, { 7, hg } }   -- BL + TBL
    matrix[4] = { { 4, 1.0 }, { 8, hg } }   -- BR + TBR
    return matrix
  end

  -- General direct-to-direct via cascade composition
  if src_def.category == "direct" and dst_def.category == "direct"
     and dst_def.channels < src_def.channels then
    local next_format = _DOWNMIX_CHAIN[src_format]
    if next_format then
      local step1 = _build_mix_matrix_core(src_format, next_format, settings)
      if step1 then
        if next_format == dst_format then
          return step1
        end
        local step2 = _build_mix_matrix_core(next_format, dst_format, settings)
        if step2 then
          return compose_matrices(step1, step2)
        end
      end
    end
  end

  return nil  -- Unsupported path
end

-- ── Extract mode matrix builder ────────────────────────────────────
-- Extract keeps only channels that map directly to the target layout.
-- Heights, center, LFE are dropped (content lost). Rears fold onto sides
-- at fixed -3dB when the target lacks separate rear speakers.
-- Applies to channel-based formats only (not ambisonics).
local _EXTRACT_HAS_CENTER = { lcr=true, ["5.1"]=true, ["7.1"]=true, ["7.1.4"]=true, ["9.1.6"]=true }

local function _build_extract_matrix_core(src_format, dst_format, settings)
  local src_def = FORMAT_DEFS[src_format]
  local dst_def = FORMAT_DEFS[dst_format]
  if not src_def or not dst_def then return nil end
  -- Extract mode is channel-based only
  if src_def.category == "ambisonic" or dst_def.category == "ambisonic" then return nil end

  local efg = _EXTRACT_FOLD_GAIN
  local matrix = {}
  for ch = 1, dst_def.channels do matrix[ch] = {} end

  -- Stereo → mono: same as downmix (left/right/downmix/downmix_lcr)
  if src_format == "stereo" and dst_format == "mono" then
    local s2m = settings.stereo_to_mono
    if s2m == "left" then
      matrix[1] = { { 1, 1.0 } }
    elseif s2m == "right" then
      matrix[1] = { { 2, 1.0 } }
    else
      matrix[1] = { { 1, 0.5 }, { 2, 0.5 } }
    end
    return matrix
  end

  -- Any direct → mono (via extract to stereo, then stereo→mono)
  -- Special case: downmix_lcr averages L+R+C directly
  if dst_format == "mono" and src_def.channels > 2 then
    if settings.stereo_to_mono == "downmix_lcr" and _EXTRACT_HAS_CENTER[src_format] then
      matrix[1] = { { 1, 1/3 }, { 2, 1/3 }, { 3, 1/3 } }
      return matrix
    end
    -- Two-stage: extract to stereo, then stereo→mono
    local stereo_m = _build_extract_matrix_core(src_format, "stereo", settings)
    if not stereo_m then return nil end
    matrix[1] = {}
    local s2m = settings.stereo_to_mono
    if s2m == "left" then
      for _, p in ipairs(stereo_m[1]) do matrix[1][#matrix[1] + 1] = { p[1], p[2] } end
    elseif s2m == "right" then
      for _, p in ipairs(stereo_m[2]) do matrix[1][#matrix[1] + 1] = { p[1], p[2] } end
    else
      for _, p in ipairs(stereo_m[1]) do matrix[1][#matrix[1] + 1] = { p[1], p[2] * 0.5 } end
      for _, p in ipairs(stereo_m[2]) do
        local found = false
        for _, ex in ipairs(matrix[1]) do
          if ex[1] == p[1] then ex[2] = ex[2] + p[2] * 0.5; found = true; break end
        end
        if not found then matrix[1][#matrix[1] + 1] = { p[1], p[2] * 0.5 } end
      end
    end
    return matrix
  end

  -- LCR → stereo: extract L,R only
  if src_format == "lcr" and dst_format == "stereo" then
    matrix[1] = { { 1, 1.0 } }  -- L
    matrix[2] = { { 2, 1.0 } }  -- R (LCR SMPTE: L=1,R=2,C=3)
    return matrix
  end

  -- Quad → stereo: extract FL,FR only
  if src_format == "quad" and dst_format == "stereo" then
    matrix[1] = { { 1, 1.0 } }  -- FL
    matrix[2] = { { 2, 1.0 } }  -- FR
    return matrix
  end

  -- 5.1 → stereo: extract L,R only
  if src_format == "5.1" and dst_format == "stereo" then
    matrix[1] = { { 1, 1.0 } }  -- L
    matrix[2] = { { 2, 1.0 } }  -- R
    return matrix
  end

  -- 5.1 → LCR: extract L,R,C
  if src_format == "5.1" and dst_format == "lcr" then
    matrix[1] = { { 1, 1.0 } }  -- L
    matrix[2] = { { 2, 1.0 } }  -- R
    matrix[3] = { { 3, 1.0 } }  -- C
    return matrix
  end

  -- 5.1 → quad: extract L,R,Ls,Rs (drop C,LFE)
  if src_format == "5.1" and dst_format == "quad" then
    matrix[1] = { { 1, 1.0 } }  -- FL = L
    matrix[2] = { { 2, 1.0 } }  -- FR = R
    matrix[3] = { { 5, 1.0 } }  -- BL = Ls
    matrix[4] = { { 6, 1.0 } }  -- BR = Rs
    return matrix
  end

  -- 7.1 → stereo: extract L,R only
  if src_format == "7.1" and dst_format == "stereo" then
    matrix[1] = { { 1, 1.0 } }
    matrix[2] = { { 2, 1.0 } }
    return matrix
  end

  -- 7.1 → LCR: extract L,R,C
  if src_format == "7.1" and dst_format == "lcr" then
    matrix[1] = { { 1, 1.0 } }
    matrix[2] = { { 2, 1.0 } }
    matrix[3] = { { 3, 1.0 } }
    return matrix
  end

  -- 7.1 → 5.1: L,R,C,LFE pass; fold rears onto sides at -3dB
  if src_format == "7.1" and dst_format == "5.1" then
    matrix[1] = { { 1, 1.0 } }                 -- L
    matrix[2] = { { 2, 1.0 } }                 -- R
    matrix[3] = { { 3, 1.0 } }                 -- C
    matrix[4] = { { 4, 1.0 } }                 -- LFE
    matrix[5] = { { 5, 1.0 }, { 7, efg } }     -- Ls = Lss + Lrs*-3dB
    matrix[6] = { { 6, 1.0 }, { 8, efg } }     -- Rs = Rss + Rrs*-3dB
    return matrix
  end

  -- 7.1 → quad: extract L,R; fold sides+rears → BL,BR; drop C,LFE
  if src_format == "7.1" and dst_format == "quad" then
    matrix[1] = { { 1, 1.0 } }                 -- FL = L
    matrix[2] = { { 2, 1.0 } }                 -- FR = R
    matrix[3] = { { 5, 1.0 }, { 7, efg } }     -- BL = Lss + Lrs*-3dB
    matrix[4] = { { 6, 1.0 }, { 8, efg } }     -- BR = Rss + Rrs*-3dB
    return matrix
  end

  -- 7.1.4 → 7.1: identity ch1-8, drop heights (ch9-12)
  if src_format == "7.1.4" and dst_format == "7.1" then
    for ch = 1, 8 do matrix[ch] = { { ch, 1.0 } } end
    return matrix
  end

  -- 7.1.4 → cube: drop C,LFE; L,R pass; fold sides+rears → BL,BR; heights pass
  -- 7.1.4: L(1),R(2),C(3),LFE(4),Lss(5),Rss(6),Lrs(7),Rrs(8),Ltf(9),Rtf(10),Ltb(11),Rtb(12)
  -- Cube:  FL(1),FR(2),BL(3),BR(4),TFL(5),TFR(6),TBL(7),TBR(8)
  if src_format == "7.1.4" and dst_format == "cube" then
    matrix[1] = { { 1, 1.0 } }                 -- FL = L
    matrix[2] = { { 2, 1.0 } }                 -- FR = R
    matrix[3] = { { 5, 1.0 }, { 7, efg } }     -- BL = Lss + Lrs*-3dB
    matrix[4] = { { 6, 1.0 }, { 8, efg } }     -- BR = Rss + Rrs*-3dB
    matrix[5] = { { 9, 1.0 } }                 -- TFL = Ltf
    matrix[6] = { { 10, 1.0 } }                -- TFR = Rtf
    matrix[7] = { { 11, 1.0 } }                -- TBL = Ltb
    matrix[8] = { { 12, 1.0 } }                -- TBR = Rtb
    return matrix
  end

  -- 7.1.4 → quad: extract L,R; fold sides+rears → BL,BR; drop C,LFE,heights
  if src_format == "7.1.4" and dst_format == "quad" then
    matrix[1] = { { 1, 1.0 } }                 -- FL = L
    matrix[2] = { { 2, 1.0 } }                 -- FR = R
    matrix[3] = { { 5, 1.0 }, { 7, efg } }     -- BL = Lss + Lrs*-3dB
    matrix[4] = { { 6, 1.0 }, { 8, efg } }     -- BR = Rss + Rrs*-3dB
    return matrix
  end

  -- 7.1.4 → LCR: extract L,R,C
  if src_format == "7.1.4" and dst_format == "lcr" then
    matrix[1] = { { 1, 1.0 } }
    matrix[2] = { { 2, 1.0 } }
    matrix[3] = { { 3, 1.0 } }
    return matrix
  end

  -- 7.1.4 → stereo: extract L,R only
  if src_format == "7.1.4" and dst_format == "stereo" then
    matrix[1] = { { 1, 1.0 } }
    matrix[2] = { { 2, 1.0 } }
    return matrix
  end

  -- cube/cube_noc → quad: identity FL,FR,BL,BR; drop heights (NO fold)
  if (src_format == "cube" or src_format == "cube_noc") and dst_format == "quad" then
    for ch = 1, 4 do matrix[ch] = { { ch, 1.0 } } end
    return matrix
  end

  -- cube/cube_noc → stereo: extract FL,FR only
  if (src_format == "cube" or src_format == "cube_noc") and dst_format == "stereo" then
    matrix[1] = { { 1, 1.0 } }
    matrix[2] = { { 2, 1.0 } }
    return matrix
  end

  -- 9.1.6 → 7.1.4: identity ear-level ch1-8; drop wides(9,10), mid-heights(13,14);
  -- corner heights: Ltf(11)→ch9, Rtf(12)→ch10, Ltb(15)→ch11, Rtb(16)→ch12
  if src_format == "9.1.6" and dst_format == "7.1.4" then
    for ch = 1, 8 do matrix[ch] = { { ch, 1.0 } } end  -- ear-level pass-through
    matrix[9]  = { { 11, 1.0 } }  -- Ltf
    matrix[10] = { { 12, 1.0 } }  -- Rtf
    matrix[11] = { { 15, 1.0 } }  -- Ltb
    matrix[12] = { { 16, 1.0 } }  -- Rtb
    return matrix
  end

  -- 9.1.6 direct extracts (stereo, LCR) — avoid cascade for simple paths
  if src_format == "9.1.6" and dst_format == "stereo" then
    matrix[1] = { { 1, 1.0 } }
    matrix[2] = { { 2, 1.0 } }
    return matrix
  end
  if src_format == "9.1.6" and dst_format == "lcr" then
    matrix[1] = { { 1, 1.0 } }
    matrix[2] = { { 2, 1.0 } }
    matrix[3] = { { 3, 1.0 } }
    return matrix
  end

  -- General direct-to-direct via cascade composition
  if src_def.category == "direct" and dst_def.category == "direct"
     and dst_def.channels < src_def.channels then
    local next_format = _EXTRACT_CHAIN[src_format]
    if next_format then
      local step1 = _build_extract_matrix_core(src_format, next_format, settings)
      if step1 then
        if next_format == dst_format then return step1 end
        local step2 = _build_extract_matrix_core(next_format, dst_format, settings)
        if step2 then return compose_matrices(step1, step2) end
      end
    end
  end

  return nil  -- Unsupported extract path
end

-- Apply Film↔SMPTE channel permutation to a mix matrix.
-- Input Film: remap source channel indices so Film-ordered input is read as SMPTE.
-- Output Film: remap destination channel positions so SMPTE matrix writes in Film order.
local function build_mix_matrix(src_format, dst_format, settings)
  local matrix
  if settings.conversion_mode == "extract" then
    matrix = _build_extract_matrix_core(src_format, dst_format, settings)
  else
    matrix = _build_mix_matrix_core(src_format, dst_format, settings)
  end
  if not matrix then return nil end

  -- Ambisonic input format conversion: N3D or FuMa -> ACN/SN3D
  local src_def = FORMAT_DEFS[src_format]
  if src_def and src_def.category == "ambisonic"
     and settings.ambi_input_format and settings.ambi_input_format ~= "ambix" then
    local order = src_def.order
    if settings.ambi_input_format == "acn_n3d" then
      -- N3D: same channel ordering as ACN, just rescale by 1/sqrt(2n+1) per order
      for dst_ch = 1, #matrix do
        if matrix[dst_ch] then
          for _, pair in ipairs(matrix[dst_ch]) do
            local acn_0 = pair[1] - 1  -- 0-indexed ACN
            local n = math.floor(math.sqrt(acn_0))  -- order of this channel
            pair[2] = pair[2] * N3D_TO_SN3D[n]  -- N3D is louder by sqrt(2n+1): scale coeff DOWN
          end
        end
      end
    elseif settings.ambi_input_format == "fuma" and order <= 3 then
      -- FuMa: reorder channels + rescale from maxN to SN3D
      local perm = FUMA_TO_ACN[order]      -- perm[fuma_1idx] = acn_1idx
      local gains = FUMA_MAXN_TO_SN3D[order]  -- gains[fuma_1idx] = maxN->SN3D factor
      -- Build inverse: acn_to_fuma[acn_1idx] = fuma_1idx
      local acn_to_fuma = {}
      for fuma_i, acn_i in ipairs(perm) do
        acn_to_fuma[acn_i] = fuma_i
      end
      for dst_ch = 1, #matrix do
        if matrix[dst_ch] then
          for _, pair in ipairs(matrix[dst_ch]) do
            local acn_1 = pair[1]
            local fuma_1 = acn_to_fuma[acn_1]
            if fuma_1 then
              pair[1] = fuma_1  -- read from FuMa position instead of ACN
              pair[2] = pair[2] * gains[fuma_1]  -- apply maxN->SN3D conversion
            end
          end
        end
      end
    end
  end

  -- Input permutation: source file is Film-ordered, remap src indices to SMPTE
  if settings.input_ordering == "film" and FILM_TO_SMPTE[src_format] then
    local remap = FILM_TO_SMPTE[src_format]
    for dst_ch = 1, #matrix do
      for _, pair in ipairs(matrix[dst_ch]) do
        -- pair[1] is SMPTE source index; need to read from Film position instead
        -- remap[film_pos] = smpte_pos, so inverse: smpte_pos → film_pos
        local inv = SMPTE_TO_FILM[src_format]
        if inv and inv[pair[1]] then
          pair[1] = inv[pair[1]]
        end
      end
    end
  end

  -- Output permutation: write channels in Film order instead of SMPTE
  if settings.output_ordering == "film" and SMPTE_TO_FILM[dst_format] then
    local remap = SMPTE_TO_FILM[dst_format]
    local permuted = {}
    for smpte_ch = 1, #matrix do
      local film_ch = remap[smpte_ch] or smpte_ch
      permuted[film_ch] = matrix[smpte_ch]
    end
    matrix = permuted
  end

  return matrix
end

local function convert_file_begin(file, target_format, mix_matrix, output_path)
  local src = io.open(file.path, "rb")
  if not src then return nil, "Cannot open source" end

  local dst = io.open(output_path, "wb")
  if not dst then src:close(); return nil, "Cannot create output" end

  local meta = wav_io.read_metadata_chunks(file.path)

  local dst_def = FORMAT_DEFS[target_format]
  local dst_channels = dst_def.channels
  local bps = file.bits_per_sample
  local bytes_per_sample = math.floor(bps / 8)

  -- Write RIFF header (placeholder sizes)
  wav_io.write_riff_header(dst)

  -- Write fmt chunk: EXTENSIBLE for >2ch (with channel mask), standard PCM for mono/stereo
  if dst_channels > 2 then
    local mask = CHANNEL_MASKS[target_format] or 0
    wav_io.write_fmt_chunk_extensible(dst, dst_channels, file.sample_rate, bps, mask)
  else
    local fmt_data = string.pack("<I2I2I4I4I2I2",
      1,                                    -- audio_format = PCM
      dst_channels,                         -- channels
      file.sample_rate,                     -- sample_rate
      file.sample_rate * dst_channels * bytes_per_sample,  -- byte_rate
      dst_channels * bytes_per_sample,      -- block_align
      bps)                                  -- bits_per_sample
    wav_io.write_fmt_chunk(dst, fmt_data)
  end

  -- Write data chunk header
  local data_size_offset = wav_io.write_data_header(dst)

  -- Seek source to PCM data
  src:seek("set", file.data_offset)

  return {
    src = src,
    dst = dst,
    data_size_offset = data_size_offset,
    src_channels = file.channels,
    src_format = file.resolved_format,
    dst_channels = dst_channels,
    dst_format = target_format,
    src_bps = bps,
    src_bytes_per_sample = bytes_per_sample,
    src_frame_size = file.channels * bytes_per_sample,
    dst_frame_size = dst_channels * bytes_per_sample,
    mix_matrix = mix_matrix,
    total_frames = math.floor(file.data_size / (file.channels * bytes_per_sample)),
    frames_written = 0,
    bytes_written = 0,
    peak_sample = 0,
    clip_count = 0,
    meta_bext = meta.bext,
    meta_ixml = meta.ixml,
    meta_list_info = meta.list_info,
    channels_changed = (file.channels ~= dst_channels),
  }
end

local function convert_tick(cs, blocks_per_frame)
  local budget_frames = math.floor(
    (CONFIG.block_size * blocks_per_frame) / cs.src_frame_size)
  local remaining = cs.total_frames - cs.frames_written
  if remaining <= 0 then return "done" end

  local actual_frames = math.min(budget_frames, remaining)
  local read_bytes = actual_frames * cs.src_frame_size
  local raw = cs.src:read(read_bytes)
  if not raw or #raw == 0 then return "done" end

  actual_frames = math.floor(#raw / cs.src_frame_size)
  if actual_frames == 0 then return "done" end

  local parts = {}
  local bps = cs.src_bps
  local bps_bytes = cs.src_bytes_per_sample
  local unpack_sample = wav_io.unpack_sample
  local pack_sample = wav_io.pack_sample
  local peak = cs.peak_sample
  local clips = cs.clip_count

  for i = 0, actual_frames - 1 do
    local frame_base = i * cs.src_frame_size + 1
    for dst_ch = 1, cs.dst_channels do
      local sum = 0
      local sources = cs.mix_matrix[dst_ch]
      if sources then
        for _, pair in ipairs(sources) do
          local src_ch, coeff = pair[1], pair[2]
          local offset = frame_base + (src_ch - 1) * bps_bytes
          sum = sum + unpack_sample(raw, offset, bps) * coeff
        end
      end
      -- Track peak and clip count
      local abs_sum = math.abs(sum)
      if abs_sum > peak then peak = abs_sum end
      if abs_sum > 1.0 then
        clips = clips + 1
        sum = sum > 0 and 1.0 or -1.0
      end
      parts[#parts + 1] = pack_sample(sum, bps)
    end
  end

  cs.peak_sample = peak
  cs.clip_count = clips

  local block = table.concat(parts)
  cs.dst:write(block)
  cs.frames_written = cs.frames_written + actual_frames
  cs.bytes_written = cs.bytes_written + #block

  return cs.frames_written >= cs.total_frames and "done" or "in_progress"
end

local function convert_finalize(cs)
  -- Patch data chunk size
  wav_io.patch_uint32_le(cs.dst, cs.data_size_offset, cs.bytes_written)

  -- Append metadata chunks from source (after data, before RIFF size patch)
  cs.dst:seek("end")

  if cs.meta_bext then
    local bext = cs.meta_bext
    if cs.channels_changed then
      bext = wav_io.strip_bext_coding_history(bext)
    end
    wav_io.write_chunk(cs.dst, "bext", bext)
  end

  if cs.meta_ixml then
    local ixml = cs.meta_ixml
    if cs.channels_changed then
      ixml = wav_io.strip_ixml_track_list(ixml)
    end
    wav_io.write_chunk(cs.dst, "iXML", ixml)
  end

  if cs.meta_list_info then
    wav_io.write_chunk(cs.dst, "LIST", cs.meta_list_info)
  end

  -- Patch RIFF chunk size (file size - 8)
  local file_size = cs.dst:seek("end")
  wav_io.patch_uint32_le(cs.dst, 4, file_size - 8)
  cs.src:close()
  cs.dst:close()
  return true
end

-- ── Processing State Machine ──────────────────────────────────────
start_processing = function(state)
  if state.output_mode == "folder" and state.output_dir == "" then
    state.footer_warning = "Set output folder first"
    state.footer_warning_ts = R.time_precise()
    return
  end

  -- Validate output folder exists and is writable
  if state.output_mode == "folder" then
    local test_path = state.output_dir:gsub("[/\\]$", "") .. "/_slice_test"
    local fh = io.open(test_path, "w")
    if fh then
      fh:close()
      os.remove(test_path)
    else
      state.footer_warning = "Output folder not writable"
      state.footer_warning_ts = R.time_precise()
      return
    end
  end

  local n = count_processable(state)
  if n == 0 then return end

  if state.output_mode == "delete" then
    local answer = R.ShowMessageBox(
      "This will replace the original files with converted versions.\n\n" ..
      "Are you sure?", "Temper Slice — Confirm Replace", 4)
    if answer ~= 6 then return end
  end

  state.status = "processing"
  state.process_idx = 1
  state.process_total = n
  state.process_results = {}
  state.process_done_count = 0
  state.process_state = nil
  state.process_current_file = nil
  state.process_current_path = nil
  state.cancel_requested = false
end

local function tick_processing(state)
  -- Cancel
  if state.cancel_requested then
    if state.process_state then
      pcall(function() state.process_state.src:close() end)
      pcall(function() state.process_state.dst:close() end)
      if state.process_current_path then
        os.remove(state.process_current_path)
      end
    end
    state.status = "idle"
    state.process_state = nil
    state.cancel_requested = false
    return
  end

  -- No active conversion — find next file
  if not state.process_state then
    while state.process_idx <= #state.files do
      local file = state.files[state.process_idx]
      if can_process_file(file) and file.target_format then
        local mix_settings = {
          conversion_mode = file.conversion_mode or "downmix",
          stereo_to_mono = file.stereo_to_mono_mode or state.stereo_to_mono,
          input_ordering = state.input_ordering,
          output_ordering = state.output_ordering,
          ambi_input_format = state.ambi_input_format,
          downmix_preset = state.downmix_preset,
          custom_atten = state.custom_atten,
        }
        local mix = build_mix_matrix(file.resolved_format, file.target_format, mix_settings)
        if not mix then
          state.process_results[file.path] = { ok = false, error = "No conversion path" }
          state.process_done_count = state.process_done_count + 1
          state.process_idx = state.process_idx + 1
        else
          local out_path = resolve_output_path(file, file.target_format,
            file.conversion_mode or "downmix", state.output_mode, state.output_dir)
          local cs, err = convert_file_begin(file, file.target_format, mix, out_path)
          if cs then
            state.process_state = cs
            state.process_current_file = file
            state.process_current_path = out_path
            return
          else
            state.process_results[file.path] = { ok = false, error = err }
            state.process_done_count = state.process_done_count + 1
            state.process_idx = state.process_idx + 1
          end
        end
      else
        state.process_idx = state.process_idx + 1
      end
    end

    -- All files processed
    state.status = "done"
    state.done_ts = R.time_precise()
    state.process_state = nil

    -- Mark completed files green
    for _, f in ipairs(state.files) do
      local r = state.process_results[f.path]
      if r and r.ok then
        f.status = (r.clip_count and r.clip_count > 0) and "clipped" or "green"
      end
    end
    return
  end

  -- Active conversion tick
  local cs = state.process_state
  local ok, result = pcall(convert_tick, cs, CONFIG.blocks_per_frame)
  if not ok then
    pcall(function() cs.src:close() end)
    pcall(function() cs.dst:close() end)
    if state.process_current_path then
      os.remove(state.process_current_path)
    end
    state.process_results[state.process_current_file.path] =
      { ok = false, error = tostring(result) }
    state.process_done_count = state.process_done_count + 1
    state.process_idx = state.process_idx + 1
    state.process_state = nil
    return
  end

  if result == "done" then
    -- Capture stats before finalize closes files
    local peak_db = cs.peak_sample > 0
      and (20 * math.log(cs.peak_sample, 10)) or -math.huge
    local clip_count = cs.clip_count

    convert_finalize(cs)

    -- DELETE mode: replace original with converted file
    if state.output_mode == "delete" then
      local orig = state.process_current_file.path
      local tmp  = state.process_current_path
      local rm_ok = os.remove(orig)
      if rm_ok then
        local rn_ok, rn_err = os.rename(tmp, orig)
        if not rn_ok then
          state.process_results[orig] =
            { ok = false, error = "Rename failed: " .. tostring(rn_err) }
          state.process_done_count = state.process_done_count + 1
          state.process_idx = state.process_idx + 1
          state.process_state = nil
          return
        end
      else
        state.process_results[orig] =
          { ok = false, error = "Cannot remove original for replacement" }
        state.process_done_count = state.process_done_count + 1
        state.process_idx = state.process_idx + 1
        state.process_state = nil
        return
      end
    end

    state.process_results[state.process_current_file.path] = {
      ok = true,
      output_path = state.process_current_path,
      peak_db = peak_db,
      clip_count = clip_count,
    }
    state.process_done_count = state.process_done_count + 1
    state.process_idx = state.process_idx + 1
    state.process_state = nil
  end
end

local function tick_state(state)
  if state.status == "processing" then
    tick_processing(state)
  elseif state.status == "done" then
    if (R.time_precise() - state.done_ts) >= CONFIG.done_display_sec then
      state.status = "idle"
    end
  end
end

