-- Film↔SMPTE channel index permutation maps
-- Film order: C immediately after L, LFE last of ear-level channels
-- SMPTE order: AES pairs (L/R, C/LFE, Ls/Rs) — WAV standard
-- Maps are 1-indexed: map[film_pos] = smpte_pos
local FILM_TO_SMPTE = {
  lcr     = { 1, 3, 2 },                       -- Film L,C,R → SMPTE L,R,C
  ["5.1"] = { 1, 3, 2, 5, 6, 4 },              -- Film L,C,R,Ls,Rs,LFE → SMPTE L,R,C,LFE,Ls,Rs
  ["7.1"] = { 1, 3, 2, 5, 6, 7, 8, 4 },        -- Film L,C,R,Lss,Rss,Lrs,Rrs,LFE → SMPTE
  ["7.1.4"] = { 1, 3, 2, 5, 6, 7, 8, 4, 9, 10, 11, 12 },  -- ear-level same as 7.1, heights pass through
  ["9.1.6"] = { 1, 3, 2, 5, 6, 7, 8, 4, 9, 10, 11, 12, 13, 14, 15, 16 },  -- ear-level same as 7.1
}

-- Compute inverse maps: SMPTE_TO_FILM[fmt][smpte_pos] = film_pos
local SMPTE_TO_FILM = {}
for fmt, fmap in pairs(FILM_TO_SMPTE) do
  local inv = {}
  for film_pos, smpte_pos in ipairs(fmap) do
    inv[smpte_pos] = film_pos
  end
  SMPTE_TO_FILM[fmt] = inv
end


