    dot_col = SC.TERTIARY
  elseif file.status == "clipped" then
    dot_col = SC.TERTIARY
  elseif file.status == "green" then
    dot_col = COL_GREEN
  else
    dot_col = SC.PRIMARY
  end
  R.ImGui_DrawList_AddCircleFilled(dl, dot_x, dot_y, CONFIG.dot_r, dot_col)

  -- Column offsets (from right edge)
  local target_w  = CONFIG.col_target_w
  local arrow_w   = CONFIG.col_arrow_w
  local format_w  = CONFIG.col_format_w
  local ch_w      = CONFIG.col_ch_w
  local name_x    = CONFIG.col_name_x
  local ch_x      = row_w - target_w - arrow_w - format_w - ch_w
  local format_x  = row_w - target_w - arrow_w - format_w
  local arrow_x   = row_w - target_w - arrow_w
  local target_x  = row_w - target_w

  -- Filename (dark background)
  local name_clip = ch_x - name_x - 4
  if name_clip > 0 then
    R.ImGui_DrawList_AddRectFilled(dl, cx + name_x - 4, cy + 2,
      cx + ch_x - 4, cy + CONFIG.row_h - 2, SC.ACTIVE_DARK, 2)
    R.ImGui_DrawList_AddText(dl, cx + name_x, cy + 4, SC.TEXT_ON,
      file.filename:sub(1, math.floor(name_clip / 7)))
  end

  -- Channel count (dark background)
  local ch_text = file.channels .. "ch"
  R.ImGui_DrawList_AddRectFilled(dl, cx + ch_x, cy + 2,
    cx + ch_x + ch_w - 4, cy + CONFIG.row_h - 2, SC.ACTIVE_DARK, 2)
  R.ImGui_DrawList_AddText(dl, cx + ch_x + 6, cy + 4, SC.TEXT_ON, ch_text)

  -- Source format
  R.ImGui_PushStyleVar(ctx, R.ImGui_StyleVar_FramePadding(), 4, 2)
  if file.is_ambiguous and file.ambiguous_options then
    -- Inline combo for ambiguous files (orange = default, teal = user-confirmed)
    R.ImGui_SetCursorPos(ctx, format_x, row_start_y + 2)
    R.ImGui_PushItemWidth(ctx, format_w - 4)
    if file.user_confirmed then
      R.ImGui_PushStyleColor(ctx, R.ImGui_Col_FrameBg(), SC.PRIMARY_AC)
      R.ImGui_PushStyleColor(ctx, R.ImGui_Col_FrameBgHovered(), SC.PRIMARY_HV)
      R.ImGui_PushStyleColor(ctx, R.ImGui_Col_Text(), SC.WINDOW)
    else
