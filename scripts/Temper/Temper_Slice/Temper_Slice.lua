  R.ImGui_PushStyleColor(ctx, R.ImGui_Col_Header(), 0)
  R.ImGui_PushStyleColor(ctx, R.ImGui_Col_HeaderHovered(), 0)
  R.ImGui_PushStyleColor(ctx, R.ImGui_Col_HeaderActive(), 0)
  if R.ImGui_Selectable(ctx, "##row_" .. idx, is_selected, sel_flags, 0, CONFIG.row_h) then
    handle_file_click(ctx, state, idx)
  end
