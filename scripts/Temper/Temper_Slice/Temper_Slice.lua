      for _, opt_key in ipairs(file.ambiguous_options) do
        local opt_def = FORMAT_DEFS[opt_key]
        if opt_def and R.ImGui_Selectable(ctx, opt_def.label .. "##src_" .. idx .. "_" .. opt_key) then
          apply_format_selection(state, idx, opt_key)
        end
      end
      R.ImGui_PopStyleColor(ctx, 1)
      R.ImGui_EndCombo(ctx)
    end

    R.ImGui_PopStyleColor(ctx, 3)
    R.ImGui_PopItemWidth(ctx)
  else
    -- Static format text with dark background
    local fmt_text = file.resolved_format and FORMAT_DEFS[file.resolved_format]
      and FORMAT_DEFS[file.resolved_format].label or "—"
    local fmt_col = file.status == "red" and SC.ERROR_RED or SC.TEXT_ON
    R.ImGui_DrawList_AddRectFilled(dl, cx + format_x, cy + 2,
      cx + format_x + format_w - 4, cy + CONFIG.row_h - 2, SC.ACTIVE_DARK, 2)
    R.ImGui_DrawList_AddText(dl, cx + format_x + 6, cy + 4, fmt_col, fmt_text)
  end
  R.ImGui_PopStyleVar(ctx, 1)  -- FramePadding

  -- Arrow separator
  R.ImGui_DrawList_AddText(dl, cx + arrow_x + 2, cy + 4, SC.TEXT_MUTED, ">")

  -- Target format
  R.ImGui_PushStyleVar(ctx, R.ImGui_StyleVar_FramePadding(), 4, 2)
  if file.status ~= "red" and #file.valid_targets > 0 then
    R.ImGui_SetCursorPos(ctx, target_x, row_start_y + 2)
    R.ImGui_PushItemWidth(ctx, target_w - 4)

    do
      -- Target format combo (dark default, teal when user-confirmed)
      -- Mono targets show their variant label (e.g. "Mono (LR)")
      local tgt_label
      if file.target_format == "mono" and file.conversion_mode == "extract" and file.stereo_to_mono_mode then
        local s2m = file.stereo_to_mono_mode
        local mk = "mono_" .. (s2m == "downmix" and "lr" or s2m == "downmix_lcr" and "lcr" or s2m)
        local mono_info = _MONO_KEYS[mk]
        tgt_label = mono_info and mono_info.label or "Mono"
      else
        tgt_label = file.target_format and FORMAT_DEFS[file.target_format]
          and FORMAT_DEFS[file.target_format].label or "Select..."
      end
      if file.conversion_mode == "extract" then
        tgt_label = tgt_label .. " (Iso)"
      end
      if file.target_confirmed then
        R.ImGui_PushStyleColor(ctx, R.ImGui_Col_FrameBg(), SC.PRIMARY_AC)
        R.ImGui_PushStyleColor(ctx, R.ImGui_Col_FrameBgHovered(), SC.PRIMARY_HV)
        R.ImGui_PushStyleColor(ctx, R.ImGui_Col_Text(), SC.WINDOW)
      else
        R.ImGui_PushStyleColor(ctx, R.ImGui_Col_FrameBg(), SC.ACTIVE_DARK)
        R.ImGui_PushStyleColor(ctx, R.ImGui_Col_FrameBgHovered(), SC.HOVER_INACTIVE)
        R.ImGui_PushStyleColor(ctx, R.ImGui_Col_Text(), SC.TEXT_ON)
      end
      if R.ImGui_BeginCombo(ctx, "##tgt_" .. idx, tgt_label,
          R.ImGui_ComboFlags_NoArrowButton()) then
        R.ImGui_PushStyleColor(ctx, R.ImGui_Col_Text(), SC.TEXT_ON)
        local has_sep = false
        for _, k in ipairs(file.valid_targets) do
          if k == "__sep_extract__" then has_sep = true; break end
        end
        if has_sep then
          R.ImGui_PushStyleColor(ctx, R.ImGui_Col_Text(), SC.TEXT_MUTED)
          R.ImGui_Text(ctx, "DOWNMIX")
          R.ImGui_PopStyleColor(ctx, 1)
        end
        for _, tgt_key in ipairs(file.valid_targets) do
          if tgt_key == "__sep_extract__" then
            R.ImGui_Separator(ctx)
            R.ImGui_PushStyleColor(ctx, R.ImGui_Col_Text(), SC.TEXT_MUTED)
            R.ImGui_Text(ctx, "ISOLATE")
            R.ImGui_PopStyleColor(ctx, 1)
          else
            local fmt, mode, mono = parse_target_key(tgt_key)
            local display
            if mono then
              display = mono.label
            else
              local tgt_def = fmt and FORMAT_DEFS[fmt]
              display = tgt_def and tgt_def.label or fmt
            end
            if mode == "extract" then display = display .. " (Iso)" end
            if display and R.ImGui_Selectable(ctx, display .. "##tgt_" .. idx .. "_" .. tgt_key) then
              apply_target_selection(state, idx, tgt_key)
            end
          end
        end
        R.ImGui_PopStyleColor(ctx, 1)
        R.ImGui_EndCombo(ctx)
      end
      R.ImGui_PopStyleColor(ctx, 3)
    end

    R.ImGui_PopItemWidth(ctx)
  else
    R.ImGui_DrawList_AddText(dl, cx + target_x, cy + 4, SC.TEXT_MUTED, "---")
  end
  R.ImGui_PopStyleVar(ctx, 1)  -- FramePadding

  -- Ensure cursor advances exactly one row height for consistent spacing
  R.ImGui_SetCursorPosY(ctx, row_start_y + CONFIG.row_h)
end

local function render_file_list(ctx, state)
  if temper_theme and temper_theme.font_bold then
    R.ImGui_PushFont(ctx, temper_theme.font_bold, 13)
  end

  local avail_w = R.ImGui_GetContentRegionAvail(ctx)

  for idx, file in ipairs(state.files) do
    render_file_row(ctx, state, idx, file, avail_w)
  end

  -- Keyboard shortcuts
  if R.ImGui_IsWindowFocused(ctx) then
    if R.ImGui_IsKeyPressed(ctx, R.ImGui_Key_Delete()) then
      remove_selected(state)
    end
    if R.ImGui_IsKeyDown(ctx, R.ImGui_Mod_Ctrl()) and
       R.ImGui_IsKeyPressed(ctx, R.ImGui_Key_A()) then
      for i = 1, #state.files do
        state.selected_indices[i] = true
      end
    end
  end

  if temper_theme and temper_theme.font_bold then R.ImGui_PopFont(ctx) end
end

