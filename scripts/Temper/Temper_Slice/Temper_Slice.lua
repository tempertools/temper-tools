      R.ImGui_PushStyleColor(ctx, R.ImGui_Col_FrameBgHovered(), SC.TERTIARY_HV)
      R.ImGui_PushStyleColor(ctx, R.ImGui_Col_Text(), SC.WINDOW)
    end

    local current_label = file.resolved_format and FORMAT_DEFS[file.resolved_format]
      and FORMAT_DEFS[file.resolved_format].label or "Select..."
